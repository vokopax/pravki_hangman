#создала функцию для игры
def play_guess_animal():
    """Основная функция игры Угадай животное"""
    #подсчитала общее количество попыток
    total_attempts = 0
    
    print("=" * 50)
    print("     Добро пожаловать в игру 'Угадай животное'!")
    print("=" * 50)
    print()
    
    #спросила пользователя, сколько раундов он хочет сыграть
    while True:
        try:
            rounds = int(input("Сколько раундов вы хотите сыграть? "))
            if rounds > 0:
                break
            else:
                print("Пожалуйста, введите положительное число.")
        except ValueError:
            print("Пожалуйста, введите корректное число.")
    
    print()
    
    #запустила игровой цикл
    for round_num in range(1, rounds + 1):
        print(f"\n--- Раунд {round_num} ---")

        #выбрала случайное животное
        animal = random.choice(list(animals.keys()))
        riddle = random.choice(animals[animal])
        
        #показала загадку
        print(f"\nЗагадка: {riddle}")
        print()
        
        #дала пользователю попытки угадать
        attempts = 0
        max_attempts = 3
        
        while attempts < max_attempts:
            attempts += 1
            total_attempts += 1
            
            guess = input(f"Попытка {attempts}/{max_attempts}. Ваш ответ: ").strip().lower()
            
            #проверила ответ
            if guess == animal:
                print(f"✓ Правильно! Это {animal.upper()}!")
                print(f"Вы угадали с {attempts} попытки!")
                break
            else:
                if attempts < max_attempts:
                    print(f"✗ Неправильно. Попробуйте ещё раз.")
                else:
                    print(f"✗ К сожалению, это был неправильный ответ.")
                    print(f"Правильный ответ: {animal.upper()}")
    
    #показала статистику
    print("\n" + "=" * 50)
    print("Игра окончена!")
    print(f"Всего использовано попыток: {total_attempts}")
    print("Спасибо за игру!")
    print("=" * 50)
    
    #спросила, хочет ли пользователь сыграть ещё раз
    print("\n" + "-" * 40)
    while True:
        again = input("Хотите сыграть еще раз в Угадай животное? (да/нет): ").strip().lower()
        
        if again in ["да", "yes", "д", "y"]:
            print()
            play_guess_animal()
            break
        elif again in ["нет", "no", "н", "n"]:
            print("\nВозвращаюсь в главное меню...")
            break
        else:
            print("Пожалуйста, введите 'да' или 'нет'.")

#запустила игру
if __name__ == "__main__":
    play_guess_animal()